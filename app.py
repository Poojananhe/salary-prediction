import streamlit as st
import pandas as pd
import pickle
from sklearn.preprocessing import LabelEncoder

# --- Load the Model ---
# Ensure the model file is in the same directory as app.py or specify the correct path
try:
    with open('linear_regression_model.pkl', 'rb') as file:
        model = pickle.load(file)
except FileNotFoundError:
    st.error("Model file 'linear_regression_model.pkl' not found. Please ensure it's in the correct directory.")
    st.stop()

# --- Streamlit App --- (Add title, description, and user input fields)
st.title('Salary Prediction App')
st.write('Enter job details to predict the salary.')

# Assuming you have the list of unique values for encoding
# In a real-world scenario, you would save and load these encoders too, or train them on the full dataset before saving.
# For demonstration, we'll recreate basic encoders with placeholder values.
# IMPORTANT: These lists should be derived from your training data for accurate encoding.

# Placeholder lists - replace with actual unique values from your training data from x_train
# Example of how you would get these from your x_train DataFrame:
# company_names = sorted(x_train['Company Name'].astype(str).unique().tolist())
company_names = ['Sasken', 'Advanced Millennium Technologies', 'Unacademy', 'SnapBizz Cloudtech', 'Appoids Tech Solutions', 'Capgemini', 'IBM India', 'Accenture', 'Wipro Technologies'] # EXTEND THIS LIST WITH ALL UNIQUE COMPANY NAMES FROM YOUR TRAINING DATA
job_titles = ['Android Developer', 'Web Developer', 'Software Engineer', 'Data Scientist', 'HR Manager', 'Project Manager'] # EXTEND THIS LIST WITH ALL UNIQUE JOB TITLES FROM YOUR TRAINING DATA
locations = ['Bangalore', 'Chennai', 'Hyderabad', 'Mumbai', 'Delhi', 'Kolkata'] # EXTEND THIS LIST WITH ALL UNIQUE LOCATIONS FROM YOUR TRAINING DATA
employment_statuses = ['Full Time', 'Contract', 'Part Time'] # EXTEND THIS LIST WITH ALL UNIQUE EMPLOYMENT STATUSES FROM YOUR TRAINING DATA
job_roles = ['Android', 'Web', 'Developer', 'Analyst', 'Manager'] # EXTEND THIS LIST WITH ALL UNIQUE JOB ROLES FROM YOUR TRAINING DATA

# Create and fit LabelEncoders for each categorical feature (must be fitted on ALL possible values)
le_company = LabelEncoder()
le_company.fit(company_names)

le_job_title = LabelEncoder()
le_job_title.fit(job_titles)

le_location = LabelEncoder()
le_location.fit(locations)

le_employment_status = LabelEncoder()
le_employment_status.fit(employment_statuses)

le_job_role = LabelEncoder()
le_job_role.fit(job_roles)


# --- Input Fields ---
rating = st.slider('Rating', 1.0, 5.0, 3.5)
company_name = st.selectbox('Company Name', company_names)
job_title = st.selectbox('Job Title', job_titles)
salaries_reported = st.number_input('Salaries Reported', min_value=1, value=5)
location = st.selectbox('Location', locations)
employment_status = st.selectbox('Employment Status', employment_statuses)
job_role = st.selectbox('Job Role', job_roles)

# --- Preprocess Input for Prediction ---
# Ensure all input features are converted to the same format as x_train used for model training.
# Categorical features need to be label encoded.
input_df = pd.DataFrame({
    'Rating': [rating],
    'Company Name': [le_company.transform([company_name])[0]],  # Encode company name
    'Job Title': [le_job_title.transform([job_title])[0]],      # Encode job title
    'Salaries Reported': [salaries_reported],
    'Location': [le_location.transform([location])[0]],        # Encode location
    'Employment Status': [le_employment_status.transform([employment_status])[0]], # Encode employment status
    'Job Roles': [le_job_role.transform([job_role])[0]]          # Encode job role
})

# --- Make Prediction ---
if st.button('Predict Salary'):
    prediction = model.predict(input_df)
    st.success(f'Predicted Salary: ₹{prediction[0]:,.2f}')

